// Command genmapping generates the JS import mapping tables for the protov1js fixer.
// It scans proto/smartcore/bos/ to build mappings from old/flat import paths to new versioned paths.
package main

import (
	"bufio"
	"bytes"
	"fmt"
	"go/format"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"

	"github.com/smart-core-os/sc-bos/internal/compat/protopkg"
)

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	rootDir, err := findRepoRoot()
	if err != nil {
		return fmt.Errorf("finding repo root: %w", err)
	}

	// uiGenMapping: old flat/trait-prefixed import -> new versioned import within sc-bos-ui-gen.
	// scApiMapping: sc-api-grpc-web subpath -> new versioned import within sc-bos-ui-gen.
	uiGenMapping := make(map[string]string)
	scApiMapping := make(map[string]string)

	protoDir := filepath.Join(rootDir, "proto", "smartcore", "bos")
	if _, err := os.Stat(protoDir); err != nil {
		return fmt.Errorf("proto/smartcore/bos directory not found: %w", err)
	}

	err = filepath.WalkDir(protoDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() || !strings.HasSuffix(path, ".proto") {
			return nil
		}

		relPath, _ := filepath.Rel(protoDir, path)
		relPath = filepath.ToSlash(relPath)

		// Only process versioned paths (containing a /vN/ segment).
		if !hasVersionSegment(relPath) {
			return nil
		}

		pkg, err := readPackageDecl(path)
		if err != nil {
			return err
		}

		// Only process smartcore.bos.* packages.
		if !strings.HasPrefix(pkg, "smartcore.bos.") {
			return nil
		}

		// Versioned import path within sc-bos-ui-gen:
		// e.g. "light/v1/light.proto" → "smartcore/bos/light/v1/light_pb"
		importBase := strings.TrimSuffix(relPath, ".proto")
		versionedPb := "smartcore/bos/" + importBase + "_pb"
		versionedGrpcWeb := "smartcore/bos/" + importBase + "_grpc_web_pb"

		// Flat name: last path segment of importBase.
		name := lastSegment(importBase)
		flatPb := name + "_pb"
		flatGrpcWeb := name + "_grpc_web_pb"

		// Add flat → versioned entries to uiGenMapping.
		addIfAbsent(uiGenMapping, flatPb, versionedPb)
		addIfAbsent(uiGenMapping, flatGrpcWeb, versionedGrpcWeb)

		cat := categoryFromPackage(pkg)

		switch cat {
		case "trait":
			// Compat entries for sc-bos-ui-gen users who had old traits/-prefixed paths.
			addIfAbsent(uiGenMapping, "traits/"+flatPb, versionedPb)
			addIfAbsent(uiGenMapping, "traits/"+flatGrpcWeb, versionedGrpcWeb)
			// sc-api-grpc-web used traits/<name>_pb paths for traits.
			addIfAbsent(scApiMapping, "traits/"+flatPb, versionedPb)
			addIfAbsent(scApiMapping, "traits/"+flatGrpcWeb, versionedGrpcWeb)

		case "types", "info":
			// sc-api-grpc-web mirrored the sc-api directory structure.
			// Strip the version segment to recover the original sc-api subpath.
			// e.g. "types/time/v1/period.proto" -> strip v1 -> "types/time/period" -> "types/time/period_pb"
			scApiPath := stripVersionSegments(strings.TrimSuffix(relPath, ".proto")) + "_pb"
			addIfAbsent(scApiMapping, scApiPath, versionedPb)

		case "driver":
			// Driver packages have no sc-api-grpc-web equivalent.
		}

		return nil
	})
	if err != nil {
		return fmt.Errorf("walking proto dir: %w", err)
	}

	return generateFile(uiGenMapping, scApiMapping)
}

func generateFile(uiGenMapping, scApiMapping map[string]string) error {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by go generate; DO NOT EDIT.\n\npackage protov1js\n\n")

	writeMap(&buf,
		"uiGenMapping",
		"maps old/flat import paths to versioned paths within @smart-core-os/sc-bos-ui-gen.\n// Generated by scanning proto/smartcore/bos/.",
		uiGenMapping,
	)
	buf.WriteString("\n")
	writeMap(&buf,
		"scApiMapping",
		"maps @smart-core-os/sc-api-grpc-web subpaths to sc-bos-ui-gen versioned paths.\n// Generated by scanning proto/smartcore/bos/.",
		scApiMapping,
	)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting generated code: %w", err)
	}

	// When invoked via go generate, CWD is the protov1js package directory.
	outputPath := "mapping_generated.go"
	if err := os.WriteFile(outputPath, formatted, 0644); err != nil {
		return fmt.Errorf("writing %s: %w", outputPath, err)
	}

	fmt.Printf("Generated %s: %d ui-gen entries, %d sc-api entries\n",
		outputPath, len(uiGenMapping), len(scApiMapping))
	return nil
}

func writeMap(buf *bytes.Buffer, name, comment string, m map[string]string) {
	fmt.Fprintf(buf, "// %s %s\nvar %s = map[string]string{\n", name, comment, name)

	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	for _, k := range keys {
		fmt.Fprintf(buf, "\t%q: %q,\n", k, m[k])
	}
	buf.WriteString("}\n")
}

// hasVersionSegment reports whether any path segment is a version like v1, v2.
func hasVersionSegment(p string) bool {
	for _, seg := range strings.Split(p, "/") {
		if protopkg.IsVersionSegment(seg) {
			return true
		}
	}
	return false
}

// stripVersionSegments removes all version segments (v1, v2, …) from a slash-separated path.
func stripVersionSegments(p string) string {
	parts := strings.Split(p, "/")
	result := parts[:0]
	for _, seg := range parts {
		if !protopkg.IsVersionSegment(seg) {
			result = append(result, seg)
		}
	}
	return strings.Join(result, "/")
}

// lastSegment returns the final slash-separated component of p.
func lastSegment(p string) string {
	if i := strings.LastIndex(p, "/"); i >= 0 {
		return p[i+1:]
	}
	return p
}

// categoryFromPackage returns the sc-api category for a smartcore.bos.* proto package.
func categoryFromPackage(pkg string) string {
	switch {
	case strings.HasPrefix(pkg, "smartcore.bos.types"):
		return "types"
	case strings.HasPrefix(pkg, "smartcore.bos.info"):
		return "info"
	case strings.HasPrefix(pkg, "smartcore.bos.driver"):
		return "driver"
	default:
		return "trait"
	}
}

// readPackageDecl scans the proto file and returns its package name.
func readPackageDecl(path string) (string, error) {
	f, err := os.Open(path)
	if err != nil {
		return "", err
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if strings.HasPrefix(line, "package ") && strings.HasSuffix(line, ";") {
			return strings.TrimSuffix(strings.TrimPrefix(line, "package "), ";"), nil
		}
	}
	if err := scanner.Err(); err != nil {
		return "", err
	}
	return "", fmt.Errorf("no package declaration found in %s", path)
}

// addIfAbsent adds key→value to m only if key is not already present (first-wins).
func addIfAbsent(m map[string]string, key, value string) {
	if _, exists := m[key]; !exists {
		m[key] = value
	}
}

func findRepoRoot() (string, error) {
	cmd := exec.Command("git", "rev-parse", "--show-toplevel")
	output, err := cmd.Output()
	if err != nil {
		return "", err
	}
	return strings.TrimSpace(string(output)), nil
}
