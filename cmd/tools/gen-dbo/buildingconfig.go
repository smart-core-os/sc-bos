package main

import (
	"hash/fnv"
	"strconv"

	"github.com/google/uuid"
	"sigs.k8s.io/yaml"

	"github.com/smart-core-os/sc-bos/pkg/dbo"
	"github.com/smart-core-os/sc-bos/pkg/proto/meterpb"
)

// buildingType is the DBO entity type every building-config must contain exactly
// one of; the instance_validator hard-errors without a FACILITIES/BUILDING entity.
const buildingType = "FACILITIES/BUILDING"

// unitFieldSegment is the trailing segment of a translation's units.key. It is
// ".units" (plural) to match UDMI, DBO's building-config examples, and DBO's own
// code comment. NB DBO's instance_validator _UDMI_UNIT_FIELD_REGEX ends in ".unit"
// (singular) and won't match plural — a bug in the validator, not here; plural is
// the documented/wire-correct form. Flip this one constant if Connect's ingest is
// confirmed to expect singular.
const unitFieldSegment = "units"

// header is prepended to generated output. It documents the placeholders that a
// human/operator must resolve before the building-config is authoritative.
const header = `# Generated by cmd/tools/gen-dbo — Google Digital Buildings building configuration.
#
# PLACEHOLDERS TO RESOLVE before use:
#   * type: a water meter gets the canonical METERS/WM_STANDARD. An energy-only
#     electricity meter has no canonical DBO type (every METERS/EM_PWM* requires
#     power_sensor), so it gets the METERS/EM_INITIAL passthrough — reclassify it (or
#     add power reporting) once its capabilities are known.
#   * cloud_device_id: BOS has no Google device-registry id, so a deterministic
#     numeric placeholder is derived from the device name. Replace each with the real
#     numeric Google device-registry id before ingest.
#
# Point names are DBO standard fields, so each translation present_value path is an
# identity mapping. units.values maps the DBO unit name to the raw device string.
`

// BuildingConfig is a Google Digital Buildings building-configuration file: entities
// keyed by GUID. Marshalled to YAML via JSON tags (sigs.k8s.io/yaml).
type BuildingConfig map[string]Entity

// Entity is one reporting device in the building config.
type Entity struct {
	Code          string                      `json:"code"`
	Type          string                      `json:"type"`
	CloudDeviceID string                      `json:"cloud_device_id,omitempty"`
	Translation   map[string]FieldTranslation `json:"translation,omitempty"`
}

// FieldTranslation maps a DBO standard field to the device's raw payload paths.
type FieldTranslation struct {
	PresentValue string `json:"present_value"`
	Units        *Units `json:"units,omitempty"`
}

// Units binds the DBO unit name to the raw unit string the device reports at the
// UDMI units path.
type Units struct {
	Key    string            `json:"key"`
	Values map[string]string `json:"values"`
}

// deviceGUID derives a stable UUIDv5 for a device from its Smart Core name, so
// re-running the generator yields the same entity keys.
func deviceGUID(name string) string {
	return uuid.NewSHA1(uuid.NameSpaceURL, []byte("smartcore-bos:"+name)).String()
}

// buildingEntity builds the mandatory FACILITIES/BUILDING entity. code must already
// satisfy the DBO building-code format (validated by the caller). It has no
// translation or cloud_device_id, and reporting entities need no connection to it.
func buildingEntity(code string) (guid string, e Entity) {
	return deviceGUID("building:" + code), Entity{Code: code, Type: buildingType}
}

// placeholderCloudDeviceID derives a deterministic numeric id from a device name.
// DBO requires cloud_device_id to be numeric ([0-9]+) and BOS has no Google
// registry id, so this stands in — stable across runs, but NOT a real registry id;
// it must be replaced before ingest (see header).
func placeholderCloudDeviceID(name string) string {
	h := fnv.New64a()
	_, _ = h.Write([]byte(name))
	return strconv.FormatUint(h.Sum64(), 10)
}

// meterEntity builds the DBO building-config entity for a meter device from its
// name and reading support. Point names are DBO standard fields (so present_value
// is an identity path); units.values maps the DBO unit name → the raw device unit.
// The entity type is derived from the meter's commodity (water ⇒ canonical
// WM_STANDARD; electricity ⇒ EM_INITIAL passthrough); cloud_device_id is a
// deterministic numeric placeholder (see header).
func meterEntity(name string, support *meterpb.MeterReadingSupport) (guid string, e Entity) {
	typeName, _ := dbo.MeterEntityType(support.GetUsageUnit())
	e = Entity{
		Code:          name,
		Type:          typeName,
		CloudDeviceID: placeholderCloudDeviceID(name),
		Translation:   map[string]FieldTranslation{},
	}
	for _, f := range dbo.MeterFields(support.GetUsageUnit(), support.GetProducedUnit()) {
		ft := FieldTranslation{
			PresentValue: "points." + f.Field + ".present_value",
		}
		if f.Unit != "" {
			ft.Units = &Units{
				Key:    "pointset.points." + f.Field + "." + unitFieldSegment,
				Values: map[string]string{f.Unit: f.RawUnit},
			}
		}
		e.Translation[f.Field] = ft
	}
	return deviceGUID(name), e
}

// Marshal renders the building config to YAML with the placeholder header. Map keys
// (entity GUIDs, translation fields, unit values) serialise in a deterministic
// order, so output is stable across runs.
func (bc BuildingConfig) Marshal() ([]byte, error) {
	body, err := yaml.Marshal(bc)
	if err != nil {
		return nil, err
	}
	return append([]byte(header), body...), nil
}
