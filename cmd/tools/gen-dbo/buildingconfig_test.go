func TestMeterEntity(t *testing.T) {
	const name = "van/uk/brum/ugs/meters/elec-main"

	t.Run("consumption and export", func(t *testing.T) {
		guid, e := meterEntity(name, &meterpb.MeterReadingSupport{UsageUnit: "kWh", ProducedUnit: "kWh"})

		assert.Equal(t, deviceGUID(name), guid, "guid must be stable")
		assert.Equal(t, name, e.Code)
		assert.Equal(t, dbo.ElectricityMeterType, e.Type)
		assert.Equal(t, placeholderCloudDeviceID(name), e.CloudDeviceID)
		assert.Regexp(t, `^[0-9]+$`, e.CloudDeviceID, "cloud_device_id must be numeric for DBO")

		require.Contains(t, e.Translation, dbo.FieldEnergyAccumulator)
		require.Contains(t, e.Translation, dbo.FieldExportedEnergyAccumulator)

		energy := e.Translation[dbo.FieldEnergyAccumulator]
		assert.Equal(t, "points.energy_accumulator.present_value", energy.PresentValue)
		require.NotNil(t, energy.Units)
		assert.Equal(t, "pointset.points.energy_accumulator.units", energy.Units.Key)
		assert.Equal(t, map[string]string{dbo.UnitKilowattHours: "kWh"}, energy.Units.Values)
	})

	t.Run("consumption only omits exported", func(t *testing.T) {
		_, e := meterEntity(name, &meterpb.MeterReadingSupport{UsageUnit: "kWh"})
		require.Contains(t, e.Translation, dbo.FieldEnergyAccumulator)
		assert.NotContains(t, e.Translation, dbo.FieldExportedEnergyAccumulator)
	})

	t.Run("unmappable unit omits the units block", func(t *testing.T) {
		_, e := meterEntity(name, &meterpb.MeterReadingSupport{UsageUnit: "blips"})
		require.Contains(t, e.Translation, dbo.FieldEnergyAccumulator)
		assert.Nil(t, e.Translation[dbo.FieldEnergyAccumulator].Units,
			"an unmappable raw unit should not fabricate a DBO unit mapping")
	})

	t.Run("water meter is canonical WM_STANDARD with a volume field", func(t *testing.T) {
		_, e := meterEntity("van/uk/brum/ugs/meters/water-main", &meterpb.MeterReadingSupport{UsageUnit: "m³"})
		assert.Equal(t, dbo.WaterMeterType, e.Type)
		require.Contains(t, e.Translation, dbo.FieldWaterVolumeAccumulator)
		assert.NotContains(t, e.Translation, dbo.FieldEnergyAccumulator)
		water := e.Translation[dbo.FieldWaterVolumeAccumulator]
		assert.Equal(t, "points.water_volume_accumulator.present_value", water.PresentValue)
		require.NotNil(t, water.Units)
		assert.Equal(t, map[string]string{dbo.UnitCubicMeters: "m³"}, water.Units.Values)
	})
}

func TestBuildingEntity(t *testing.T) {
	guid, e := buildingEntity("UK-BRUM-UGS1")
	assert.Equal(t, buildingType, e.Type)
	assert.Equal(t, "UK-BRUM-UGS1", e.Code)
	assert.Empty(t, e.CloudDeviceID, "the building entity is not a reporting device")
	assert.Empty(t, e.Translation)
	assert.Equal(t, guid, deviceGUID("building:UK-BRUM-UGS1"), "guid must be stable")
}

func TestBuildingConfigMarshalRoundTrips(t *testing.T) {
	const name = "meter-1"
	guid, e := meterEntity(name, &meterpb.MeterReadingSupport{UsageUnit: "kWh"})
	bc := BuildingConfig{guid: e}

	data, err := bc.Marshal()
	require.NoError(t, err)

	out := string(data)
	assert.True(t, strings.HasPrefix(out, "# Generated by cmd/tools/gen-dbo"), "header comment present")
	assert.Contains(t, out, "type: "+dbo.ElectricityMeterType)
	assert.Contains(t, out, "energy_accumulator:")
	assert.Contains(t, out, "present_value: points.energy_accumulator.present_value")

	// Comments are ignored by YAML, so the whole document round-trips.
	var got BuildingConfig
	require.NoError(t, yaml.Unmarshal(data, &got))
	require.Contains(t, got, guid)
	assert.Equal(t, e.Type, got[guid].Type)
	assert.Equal(t, e.Code, got[guid].Code)
	require.Contains(t, got[guid].Translation, dbo.FieldEnergyAccumulator)
}

// TestGoldenBuildingConfig pins the generator's output for a representative pair of
// meters (consumption-only and import+export) against a committed golden file. The
// golden file doubles as a ready-to-validate sample for DBO's instance_validator.
// Regenerate with: UPDATE_GOLDEN=1 go test ./cmd/tools/gen-dbo/...
func TestGoldenBuildingConfig(t *testing.T) {
	bc := BuildingConfig{}
	bguid, building := buildingEntity("UK-BRUM-UGS1")
	bc[bguid] = building
	for name, support := range map[string]*meterpb.MeterReadingSupport{
		"van/uk/brum/ugs/meters/elec-main":  {UsageUnit: "kWh"},
		"van/uk/brum/ugs/meters/solar-pv":   {UsageUnit: "kWh", ProducedUnit: "kWh"},
		"van/uk/brum/ugs/meters/water-main": {UsageUnit: "m³"},
	} {
		guid, entity := meterEntity(name, support)
		bc[guid] = entity
	}

	data, err := bc.Marshal()
	require.NoError(t, err)

	golden := filepath.Join("testdata", "building-config.golden.yaml")
	if os.Getenv("UPDATE_GOLDEN") != "" {
		require.NoError(t, os.MkdirAll("testdata", 0o755))
		require.NoError(t, os.WriteFile(golden, data, 0o644))
		return
	}

	want, err := os.ReadFile(golden)
	require.NoError(t, err, "golden missing; regenerate with UPDATE_GOLDEN=1")
	assert.Equal(t, string(want), string(data), "output drifted; regenerate with UPDATE_GOLDEN=1")
}
