func convertEventToGrant(event Event) (accesspb.AccessAttempt_Grant, string) {
	code, ok := eventCodes[event.EventType]

	if !ok {
		return accesspb.AccessAttempt_GRANT_UNKNOWN, ""
	}

	if strings.Contains(strings.ToLower(code), "permitted") {
		return accesspb.AccessAttempt_GRANTED, code
	}

	if strings.Contains(strings.ToLower(code), "denied") {
		return accesspb.AccessAttempt_DENIED, code
	}

	return accesspb.AccessAttempt_GRANT_UNKNOWN, code
}

func convertEventToEventTypeEnum(event Event) securityeventpb.SecurityEvent_EventType {
	code, ok := eventCodes[event.EventType]

	if !ok {
		return securityeventpb.SecurityEvent_EVENT_TYPE_UNKNOWN
	}

	if strings.Contains(strings.ToLower(code), "permitted") {
		return securityeventpb.SecurityEvent_ACCESS_GRANTED
	}

	if strings.Contains(strings.ToLower(code), "denied") {
		return securityeventpb.SecurityEvent_ACCESS_DENIED
	}

	switch event.EventType {
	case 90:
		return securityeventpb.SecurityEvent_TAMPER
	case 103:
		return securityeventpb.SecurityEvent_TAMPER_CLEAR
	case 93:
		return securityeventpb.SecurityEvent_DOOR_OPEN_TOO_LONG
	case 91:
		return securityeventpb.SecurityEvent_POWER_FAILURE
	default:
		return securityeventpb.SecurityEvent_EVENT_TYPE_UNKNOWN
	}
}

